package controle;

public class While {

	public static void main(String[] args) {
		
		int contador = 1;
		
		while(contador <= 10) 
		{
			System.out.println("Boa noite");
			contador++;
		}
			
		System.out.println("");
		
		for (int c = 0; c <= 10; c++)
		{
			System.out.println(c);
		}
		
		System.out.println("");
		
		
	
	
		}
		
	}
	

